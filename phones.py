"""Единый разбор и валидация телефонных номеров (формат Казахстана).

Один источник правды для всего приложения — чтобы ключ клиента (бонусы, сессии,
брони, блокировки) был одинаковым независимо от того, как номер ввели.
"""
import re

# Текст ошибки для API/UI при некорректном номере.
PHONE_ERROR = "Введите корректный номер телефона: 11 цифр, например 77001234567."


def normalize_phone(phone: str) -> str:
    """Только цифры; ведущая 8 (формат 8XXXXXXXXXX) приводится к 7XXXXXXXXXX.

    Так '8 705 123 45 67' и '+7 705 123 45 67' дают один и тот же ключ.
    """
    d = re.sub(r"\D", "", phone or "")
    if len(d) == 11 and d[0] == "8":
        d = "7" + d[1:]
    return d


def is_valid_phone(phone: str) -> bool:
    """Корректный номер: ровно 11 цифр, начинается с 7 (после нормализации 8→7)."""
    d = normalize_phone(phone)
    return len(d) == 11 and d[0] == "7"
